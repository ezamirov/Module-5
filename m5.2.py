# This program prints numbers from a list and their squares

numbers = [12, 10, 32, 3, 66, 17, 42, 99, 20]

# a. Print each number on a new line
print("Each number:")
for num in numbers:
    print(num)

# b. Print each number and its square
print("\nEach number and its square:")
for num in numbers:
    print(f"{num} squared is {num**2}")